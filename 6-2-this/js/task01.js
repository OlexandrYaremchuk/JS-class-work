const pokemon = {
    name: ' ',
    type: 'drago',
    hp: 9000,
    isKnockedOut: true ? 'живий' : 'вбитий',
    logStatus() {
        console.log(
            `${this.name} ${this.type} ${this.isKnockedOut} його здоровʼя ${this.hp},`,
        );
    },
    healthPokemon(health) {
        this.hp -= health;
        if (this.hp <= 0) {
            this.isKnockedOut = false ? 'живий' : 'мертвий';
            this.hp = 0;
        }
    },
};

pokemon.healthPokemon(1000);
pokemon.healthPokemon(1000);
pokemon.healthPokemon(3000);
pokemon.healthPokemon(4000);
pokemon.logStatus();

