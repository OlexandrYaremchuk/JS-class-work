class User {
    constructor(firstName, lastName, age, email) {
        this.firstName = firstName;
        this.lastName = lastName;
        if (age >= 18 && age <= 120) {
            this.age = age;
        } else {
            this.age = 18;
        }
        this.email = email;
    }

    get age() {
        return this._age;
    }

    set age(value) {
        if (value >= 18 && value <= 120) {
            this._age = value;
        }
    }

    get email() {
        return this._email;
    }

    set email(value) {
        if (value.includes('@')) {
            this._email = value;
        }
    }

    printInfo() {
        console.log(`Ім'я: ${this.firstName}`);
        console.log(`Прізвище: ${this.lastName}`);
        console.log(`Вік: ${this.age}`);
        console.log(`Електронна пошта: ${this.email}`);
    }

    changeEmail(newEmail) {
        if (newEmail.includes('@')) {
            this.email = newEmail;
        }
    }
}

const user = new User('Bobi', 'Babul', 25, 'bobiBabul@gmail.com');
user.printInfo();
user.changeEmail('test@example.com');
user.printInfo();

